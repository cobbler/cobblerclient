// Check will run the "cobbler check" action and list all possible points for improvements on server side as a
// return value.
func (c *Client) Check() (*[]string, error) {
	var checks []string
	result, err := c.Call("check", c.Token)

	for _, check := range result.([]interface{}) {
		checks = append(checks, check.(string))
	}

	return &checks, err
}

